app.MapGet("/health", () => Results.Ok(new { status = "healthy" }));

app.MapPost("/auth/token", (TokenRequest req) =>
{
    if (req.Username != "admin" || req.Password != "admin")
        return Results.Unauthorized();

    var expiresAt = DateTime.UtcNow.AddMinutes(jwtMinutes);
    var jwt = new JwtSecurityToken(
        issuer: jwtIssuer,
        audience: jwtAudience,
        claims: [new Claim(ClaimTypes.Name, req.Username)],
        expires: expiresAt,
        signingCredentials: new SigningCredentials(signingKey, SecurityAlgorithms.HmacSha256));

    var token = new JwtSecurityTokenHandler().WriteToken(jwt);
    return Results.Ok(new TokenResponse(token, expiresAt));
});

// GET /api/articles — filter (name partial/ci, category exact) + clamped pagination.
app.MapGet("/api/articles", async (
    AppDb db, string? name, string? category, int? page, int? pageSize) =>
{
    var query = db.Articles.AsNoTracking().AsQueryable();

    if (!string.IsNullOrWhiteSpace(name))
    {
        var esc = name.Replace("\\", "\\\\").Replace("%", "\\%").Replace("_", "\\_");
        query = query.Where(a => EF.Functions.ILike(a.Name, $"%{esc}%"));
    }
    if (!string.IsNullOrWhiteSpace(category))
        query = query.Where(a => a.Category == category);

    var totalCount = await query.CountAsync();
    var p = Math.Max(1, page ?? 1);
    var ps = Math.Clamp(pageSize ?? 10, 1, 100);           // clamp, never reject
    var items = await query
        .OrderBy(a => a.ArticleId)
        .Skip((p - 1) * ps).Take(ps)
        .ToListAsync();
    var totalPages = (int)Math.Ceiling(totalCount / (double)ps);

    return Results.Ok(new
    {
        items = items.Select(ToRead).ToList(),
        page = p,
        pageSize = ps,
        totalCount,
        totalPages,
        hasPrevious = p > 1,
        hasNext = p < totalPages,
    });
}).RequireAuthorization();

// POST /api/articles — create.
app.MapPost("/api/articles", async (AppDb db, ArticleWrite body) =>
{
    var errors = Validate(body, isUpdate: false);
    if (errors is not null) return Results.ValidationProblem(errors);

    var a = new Article
    {
        Name = body.Name,
        Description = body.Description,
        Category = body.Category,
        Price = body.Price,
        Currency = body.Currency,
    };
    db.Articles.Add(a);
    await db.SaveChangesAsync();

    return Results.Created($"/api/articles/{a.ArticleId}", ToRead(a));
}).RequireAuthorization();

// GET /api/articles/{id}
app.MapGet("/api/articles/{id:int}", async (AppDb db, int id) =>
{
    var a = await db.Articles.AsNoTracking().FirstOrDefaultAsync(x => x.ArticleId == id);
    return a is null ? NotFound(id) : Results.Ok(ToRead(a));
}).RequireAuthorization();

// PUT /api/articles/{id} — full update with optimistic concurrency on xmin.
app.MapPut("/api/articles/{id:int}", async (AppDb db, int id, ArticleWrite body) =>
{
    var errors = Validate(body, isUpdate: true);
    if (errors is not null) return Results.ValidationProblem(errors);

    var a = await db.Articles.FindAsync(id);
    if (a is null) return NotFound(id);

    // EF compares this original xmin against the current DB value on save.
    db.Entry(a).Property(x => x.RowVersion).OriginalValue = body.RowVersion!.Value;
    a.Name = body.Name;
    a.Description = body.Description;
    a.Category = body.Category;
    a.Price = body.Price;
    a.Currency = body.Currency;

    try
    {
        await db.SaveChangesAsync();
    }
    catch (DbUpdateConcurrencyException)
    {
        return Results.Problem(
            statusCode: 409, title: "Conflict",
            detail: $"Article with ID {id} was modified by another request. Please retry.",
            type: "https://httpstatuses.com/409");
    }

    return Results.Ok(ToRead(a));
}).RequireAuthorization();

// DELETE /api/articles/{id}
app.MapDelete("/api/articles/{id:int}", async (AppDb db, int id) =>
{
    var a = await db.Articles.FindAsync(id);
    if (a is null) return NotFound(id);

    db.Articles.Remove(a);
    await db.SaveChangesAsync();
    return Results.NoContent();
}).RequireAuthorization();

// POST /api/articles-concurrent — batch create (array in, array out, same order).
app.MapPost("/api/articles-concurrent", async (AppDb db, List<ArticleWrite> body) =>
{
    if (body is null || body.Count == 0)
        return Results.ValidationProblem(new Dictionary<string, string[]>
        {
            ["items"] = ["At least one article is required."],
        });
    if (body.Count > 100)
        return Results.ValidationProblem(new Dictionary<string, string[]>
        {
            ["items"] = ["Cannot create more than 100 articles at once."],
        });

    for (var i = 0; i < body.Count; i++)
    {
        var errors = Validate(body[i], isUpdate: false);
        if (errors is not null)
            return Results.ValidationProblem(
                errors.ToDictionary(kv => $"[{i}].{kv.Key}", kv => kv.Value));
    }

    var entities = body.Select(w => new Article
    {
        Name = w.Name,
        Description = w.Description,
        Category = w.Category,
        Price = w.Price,
        Currency = w.Currency,
    }).ToList();

    db.Articles.AddRange(entities);
    await db.SaveChangesAsync();

    return Results.Created("/api/articles", entities.Select(ToRead).ToList());
}).RequireAuthorization();

app.Run();

