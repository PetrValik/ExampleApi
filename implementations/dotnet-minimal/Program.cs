var builder = WebApplication.CreateBuilder(args);

// --- Config (env overrides via Jwt__SecretKey, ConnectionStrings__Default, ...) ---
var cfg = builder.Configuration;
var jwtIssuer = cfg["Jwt:Issuer"] ?? "ExampleApi";
var jwtAudience = cfg["Jwt:Audience"] ?? "ExampleApiClient";
var jwtSecret = cfg["Jwt:SecretKey"] ?? "CHANGE-THIS-IN-PRODUCTION-use-a-long-random-secret-key";
var jwtMinutes = int.TryParse(cfg["Jwt:ExpirationMinutes"], out var mins) ? mins : 60;
if (jwtSecret.Length < 32)
    throw new InvalidOperationException("Jwt:SecretKey must be at least 32 characters for HS256.");
var connString = cfg.GetConnectionString("Default")
    ?? "Host=localhost;Port=5432;Database=exampleapi;Username=postgres;Password=postgres";
var signingKey = new SymmetricSecurityKey(Encoding.UTF8.GetBytes(jwtSecret));

// --- Services ---
builder.Services.AddDbContext<AppDb>(o => o.UseNpgsql(connString));
builder.Services.AddProblemDetails();
builder.Services
    .AddAuthentication(JwtBearerDefaults.AuthenticationScheme)
    .AddJwtBearer(o => o.TokenValidationParameters = new TokenValidationParameters
    {
        ValidateIssuer = true,
        ValidateAudience = true,
        ValidateLifetime = true,
        ValidateIssuerSigningKey = true,
        ValidIssuer = jwtIssuer,
        ValidAudience = jwtAudience,
        IssuerSigningKey = signingKey,
    });
builder.Services.AddAuthorization();

var app = builder.Build();

// --- Schema at startup: retry-connect until Postgres is ready, then CREATE TABLE.
//     xmin is a built-in PostgreSQL system column, so we never create it ourselves;
//     EF only reads it as the optimistic-concurrency token.
const string createSql = """
    CREATE TABLE IF NOT EXISTS articles (
        article_id  integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        name        varchar(64)   NOT NULL,
        description varchar(2048)  NOT NULL,
        category    varchar(64)    NULL,
        price       numeric(18,2)  NOT NULL,
        currency    varchar(3)     NULL
    );
    """;
using (var scope = app.Services.CreateScope())
{
    var db = scope.ServiceProvider.GetRequiredService<AppDb>();
    for (var attempt = 1; ; attempt++)
    {
        try { await db.Database.ExecuteSqlRawAsync(createSql); break; }
        catch when (attempt < 15) { await Task.Delay(TimeSpan.FromSeconds(2)); }
    }
}

app.UseAuthentication();
app.UseAuthorization();

// --- Supported ISO-4217 currency codes (49). ---
var currencies = new HashSet<string>(StringComparer.OrdinalIgnoreCase)
{
    "USD","EUR","JPY","GBP","CHF","CAD","AUD","NZD","SEK","NOK","DKK","CZK","PLN","HUF","RON","BGN",
    "ISK","CNY","HKD","SGD","KRW","INR","THB","MYR","IDR","PHP","TWD","VND","BRL","MXN","ARS","CLP",
    "COP","PEN","AED","SAR","ILS","QAR","KWD","BHD","ZAR","TRY","RUB","UAH","EGP","NGN","KES","MAD","PKR",
};

// --- Inline helpers (local functions — the whole "domain layer"). ---
static ArticleRead ToRead(Article a) => new()
{
    ArticleId = a.ArticleId,
    Name = a.Name,
    Description = a.Description,
    Category = a.Category,
    Price = a.Price,
    Currency = a.Currency,
    RowVersion = a.RowVersion,
};

static IResult NotFound(int id) => Results.Problem(
    statusCode: 404, title: "Not Found",
    detail: $"Article with ID {id} was not found.",
    type: "https://httpstatuses.com/404");

// Returns null when valid, otherwise a field -> messages map (RFC 7807 "errors").
Dictionary<string, string[]>? Validate(ArticleWrite a, bool isUpdate)
{
    var errors = new Dictionary<string, List<string>>();
    void Add(string key, string msg)
    {
        if (!errors.TryGetValue(key, out var list)) errors[key] = list = new();
        list.Add(msg);
    }

    if (string.IsNullOrEmpty(a.Name)) Add("name", "Name is required.");
    else if (a.Name.Length > 64) Add("name", "Name must not exceed 64 characters.");

    if (string.IsNullOrEmpty(a.Description)) Add("description", "Description is required.");
    else if (a.Description.Length > 2048) Add("description", "Description must not exceed 2048 characters.");

    if (!string.IsNullOrEmpty(a.Category) && a.Category.Length > 64)
        Add("category", "Category must not exceed 64 characters.");

    if (a.Price < 0) Add("price", "Price must be greater than or equal to 0.");
    else if (a.Price > 9_999_999_999_999_999.99m) Add("price", "Price must not exceed 9,999,999,999,999,999.99.");

    if (a.Price > 0)
    {
        if (string.IsNullOrEmpty(a.Currency))
            Add("currency", "Currency is required when price is greater than 0.");
        else if (a.Currency.Length != 3 || !currencies.Contains(a.Currency))
            Add("currency", "Currency must be a supported ISO 4217 code.");
    }

    if (isUpdate && !(a.RowVersion.HasValue && a.RowVersion.Value >= 1))
        Add("row_version", "Row version is required and must be a non-zero value.");

    return errors.Count == 0 ? null : errors.ToDictionary(kv => kv.Key, kv => kv.Value.ToArray());
}

