/// <summary>EF entity backing the <c>articles</c> table.</summary>
public sealed class Article
{
    public int ArticleId { get; set; }
    public string Name { get; set; } = string.Empty;
    public string Description { get; set; } = string.Empty;
    public string? Category { get; set; }
    public decimal Price { get; set; }
    public string? Currency { get; set; }

    /// <summary>Maps to the PostgreSQL <c>xmin</c> system column (concurrency token).</summary>
    public uint RowVersion { get; set; }
}

/// <summary>Minimal EF Core context — one entity, snake_case columns, xmin as row version.</summary>
public sealed class AppDb(DbContextOptions<AppDb> options) : DbContext(options)
{
    public DbSet<Article> Articles => Set<Article>();

    protected override void OnModelCreating(ModelBuilder b)
    {
        b.Entity<Article>(e =>
        {
            e.ToTable("articles");
            e.HasKey(a => a.ArticleId);
            e.Property(a => a.ArticleId).HasColumnName("article_id").ValueGeneratedOnAdd();
            e.Property(a => a.Name).HasColumnName("name").IsRequired().HasMaxLength(64);
            e.Property(a => a.Description).HasColumnName("description").IsRequired().HasMaxLength(2048);
            e.Property(a => a.Category).HasColumnName("category").HasMaxLength(64);
            e.Property(a => a.Price).HasColumnName("price").HasColumnType("numeric(18,2)");
            e.Property(a => a.Currency).HasColumnName("currency").HasMaxLength(3);

            // xmin: PostgreSQL system column, auto-incremented on every row change.
            e.Property(a => a.RowVersion)
                .HasColumnName("xmin").HasColumnType("xid")
                .ValueGeneratedOnAddOrUpdate()
                .IsRowVersion()
                .IsConcurrencyToken();
        });
    }
}

/// <summary>Create / update / batch request body (snake_case wire fields).</summary>
public sealed class ArticleWrite
{
    [JsonPropertyName("name")] public string Name { get; set; } = string.Empty;
    [JsonPropertyName("description")] public string Description { get; set; } = string.Empty;
    [JsonPropertyName("category")] public string? Category { get; set; }
    [JsonPropertyName("price")] public decimal Price { get; set; }
    [JsonPropertyName("currency")] public string? Currency { get; set; }

    /// <summary>Required on update only; ignored for create/batch.</summary>
    [JsonPropertyName("row_version")] public uint? RowVersion { get; set; }
}

/// <summary>Article response body (snake_case wire fields).</summary>
public sealed class ArticleRead
{
    [JsonPropertyName("article_id")] public int ArticleId { get; set; }
    [JsonPropertyName("name")] public string Name { get; set; } = string.Empty;
    [JsonPropertyName("description")] public string Description { get; set; } = string.Empty;
    [JsonPropertyName("category")] public string? Category { get; set; }
    [JsonPropertyName("price")] public decimal Price { get; set; }
    [JsonPropertyName("currency")] public string? Currency { get; set; }
    [JsonPropertyName("row_version")] public uint RowVersion { get; set; }
}

/// <summary>Token request (camelCase wire fields).</summary>
public sealed record TokenRequest(string Username, string Password);

/// <summary>Token response (camelCase wire fields).</summary>
public sealed record TokenResponse(
    [property: JsonPropertyName("token")] string Token,
    [property: JsonPropertyName("expiresAt")] DateTime ExpiresAt);
